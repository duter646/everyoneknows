def infer_vector(domain, tags):
    # 用一个字典暂存累加结果，以应对题目触发多条Tag规则
    merged_vector = {}
    
    # 1. 尝试基于 Tag 捕捉精细权重
    matched_any_tag = False
    for tag in tags:
        for keyword, vec in TAG_RULES.items():
            if keyword in tag:
                matched_any_tag = True
                for major, score in vec.items():
                    merged_vector[major] = max(merged_vector.get(major, 0), score) # 取最大值避免同门类重复刷分

    # 2. 从 Domain 中加载并做融合补充 (Tag 没覆盖到的部分，Domain的属性也加上去)
    domain_vec = DOMAIN_DEFAULTS.get(domain, {"交叉/生活学科": 1.0})
    for major, score in domain_vec.items():
        if not matched_any_tag:
             merged_vector[major] = score
        else:
             # 如果 tag 触发了，但领域自带的其他特色专业也能补一点，比如数学题tag有AI，那理学属性也该在
             merged_vector[major] = max(merged_vector.get(major, 0), score * 0.5) 

    # 取小数点后两位，清理 0
    return {k: round(v, 2) for k, v in merged_vector.items() if v > 0}

def main():
    # 遍历所有题库 JSON
    files = glob.glob("../data/*_batch.json")
    if not files:
        # 尝试在当前目录找一下（适配不同的执行路径）
        files = glob.glob("data/*_batch.json")
        
    count = 0
    for file_path in files:
        with open(file_path, "r", encoding="utf-8") as f:
            data = json.load(f)
            
        modified = False
        for item in data:
            # 无论原来有没有，都重新算一遍赋予 vector
            domain = item.get("domain", "")
            tags = item.get("tags", [])
            
            old_vector = item.get("vector")
            new_vector = infer_vector(domain, tags)
            
            if old_vector != new_vector:
                item["vector"] = new_vector
                modified = True
                count += 1
                
        if modified:
            with open(file_path, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=4)
            print(f"[Done] Updated vectors in {file_path}")

    print(f"Total updated questions: {count}")

if __name__ == "__main__":
    main()
