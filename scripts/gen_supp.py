import json, os

def make_qs(start_id, domain, q_list):
    res = []
    for i, q in enumerate(q_list):
        opts = q[1]
        ans = q[2]
        if isinstance(ans, str): ans = [ord(c)-65 for c in ans]
        res.append({
            "id": f"{start_id}_{i+1:03d}",
            "domain": domain,
            "type": "single" if len(ans)==1 else "multiple",
            "question": q[0],
            "options": opts,
            "answer": ans,
            "difficulty": q[3],
            "explanation": q[4],
            "tags": q[5]
        })
    return res

